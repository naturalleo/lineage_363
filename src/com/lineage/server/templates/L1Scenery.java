package com.lineage.server.templates;

public class L1Scenery {

    private int _gfxid;

    private int _locx;

    private int _locy;

    private int _heading;

    private int _mapid;

    private String _html;

    /**
     * @return 传出 _gfxid
     */
    public int get_gfxid() {
        return this._gfxid;
    }

    /**
     * @param gfxid
     *            对 _gfxid 进行设置
     */
    public void set_gfxid(final int gfxid) {
        this._gfxid = gfxid;
    }

    /**
     * @return 传出 _locx
     */
    public int get_locx() {
        return this._locx;
    }

    /**
     * @param locx
     *            对 _locx 进行设置
     */
    public void set_locx(final int locx) {
        this._locx = locx;
    }

    /**
     * @return 传出 _locy
     */
    public int get_locy() {
        return this._locy;
    }

    /**
     * @param locy
     *            对 _locy 进行设置
     */
    public void set_locy(final int locy) {
        this._locy = locy;
    }

    /**
     * @return 传出 _heading
     */
    public int get_heading() {
        return this._heading;
    }

    /**
     * @param heading
     *            对 _heading 进行设置
     */
    public void set_heading(final int heading) {
        this._heading = heading;
    }

    /**
     * @return 传出 _mapid
     */
    public int get_mapid() {
        return this._mapid;
    }

    /**
     * @param mapid
     *            对 _mapid 进行设置
     */
    public void set_mapid(final int mapid) {
        this._mapid = mapid;
    }

    /**
     * @return 传出 _html
     */
    public String get_html() {
        return this._html;
    }

    /**
     * @param html
     *            对 _html 进行设置
     */
    public void set_html(final String html) {
        this._html = html;
    }

}
